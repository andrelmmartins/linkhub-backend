import nodemailer from 'nodemailer'

import { FORGOT_ROUTE, SENDER, SENDER_PASS, FRONTEND } from './variables'

const transport = nodemailer.createTransport({
    service: 'gmail',
    auth: {
        user: SENDER,
        pass: SENDER_PASS
    },
})

transport.verify().catch(console.error)

export async function sendForgotMail(id: string, email: string, token: string) : Promise<boolean> {
    
    return await transport.sendMail({
        from: `Linkhub ${SENDER}`,
        sender: SENDER,
        to: email,
        subject: 'Linkhub | Recuperação de Senha',
        html: generateEmail(id, token)
    })
    .then(() => {
        return true
    })
    .catch((e) => {
        console.log(e)
        return false
    })
}

